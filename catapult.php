				<p>The overhaul of Catapult's site began by researching ~50 design firms in the United States and internationally. A common trend became obvious: tiling or using cards to list projects. After recognizing this trend, Catapult made the decision to be unique and stay away from common trends, and from this, a truly different portfolio was developed. Contrary to a majority of websites, we placed the navigation at the bottom of the page, allowing the work to be the main focus of each page. Catapult's "Our Work" page, split the page into two sections: one to list all the projects, and another to display an image of the project upon hover.</p>
				<p>Coding this website was quite challenging. With responsive code being so prominent, I had to figure out a way to allow for static images of the project in the mobile version while still allowing for the hover features in the desktop version. With little jQuery/JS experience, getting the functionality of hovering on a project to change the image in another container was challenging, but ultimately fruitful. Catapult's new website was a great accumulation and test of all the coding skills's I'd been diligently learning independently.</p>
