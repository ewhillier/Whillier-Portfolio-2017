				<p class="landAppearDetails">The spacious interior of the Sun Devil Stadium could be virtually anything. A bar, a game room, a rock wall, a pet sanctuary, a DIY shop, a market, an art gallery&mdash;the sky is the limit. However, in every scenario the same challenge persists: how do you create a comfortable outdoor environment in the unrelenting heat of the Tempe sun? Regardless of the entertainment contained within the stadium, the reality is that heat is a massive obstacle in obtaining visitors. My solution to this issue is a multi-faceted system for cooling the air running through the stadium.</p>
				<h2 class="landAppearCompany">Material Usage</h2>
				<p class="landAppearDetails"> The Urban Heat Island (UHI) effect results in an increase of energy usage which can strain energy resources. A few of these solutions can mitigate the UHI through non-energy-consuming means. Firstly, material usage. User lighter colored recycled plastic materials for flooring as well as furniture has multiple benefits. It's sustainable, durable outdoors, and reflects heat away instead of absorbing it.</p>
				<h2 class="landAppearCompany">Green Roofs</h2>
				<p class="landAppearDetails">Green roofs can significantly lower the temperature of a structure. Through daily dew and evaporation cycles, plants on vertical as well as horizontal surfaces are able to cool cities during hot summer months and reduce the UHI effect. The light absorbed by vegetation would otherwise be converted to heat.</p>
				<h2 class="landAppearCompany">Shade Structures</h2>
				<p class="landAppearDetails">Shade structures have many benefits, including relief from wind, dust, the sun, and rain. These structures keep the sun off of the materials humans come into direct contact with, increasing comfort in the outdoors while also adding to the beauty of the stadium.</p>
				<h2 class="landAppearCompany">Ambient Air Cooling</h2>
				<p class="landAppearDetails">While all of these solutions take away a part of the heat issue, they are not enough to reduce the temperature significantly. In addition to these solutions, the Co365 mechanized system drastically cools the air distributed throughout the stadium via crushed ice wind tunnels. By freezing water at night, the cost of energy consumption is lowered and during the day, air is passed through a wind tunnel containing the ice to drastically cool down the air being distributed. The resulting cold water is passed through tubes in the concrete floors, cooling up to a meter of air above the ground, which is then collected and used for landscaping and cleaning.</p>
